SELECT *
FROM movies
WHERE movie_id IN   -- Select movie IDs from the inner query
	(SELECT movie_id
	FROM renting
	GROUP BY movie_id
	HAVING COUNT(*) > 5)
	
	
SELECT *
FROM customers
